#ifndef BCPUSUBTARGET_H
#define BCPUSUBTARGET_H

#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"

#include "BCpuFrameLowering.h"
#include "BCpuISelLowering.h"
#include "BCpuInstrInfo.h"

#include "BCpu.h"

#define GET_SUBTARGETINFO_HEADER
#include "BCpuGenSubtargetInfo.inc"

namespace llvm {

class BCpuSubtarget : public BCpuGenSubtargetInfo {
  virtual void anchor() {}

  BCpuRegisterInfo RegInfo;
  BCpuInstrInfo InstrInfo;
  BCpuFrameLowering FrameLowering;
  BCpuTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  BCpuSubtarget(const Triple &TT, const StringRef &CPU, const StringRef &FS,
                const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options. Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const BCpuInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const BCpuFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const BCpuTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const BCpuRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // namespace llvm

#endif // BCPUSUBTARGET_H
